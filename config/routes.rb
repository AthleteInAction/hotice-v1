Rails.application.routes.draw do

  root 'splash#index'

  resources :users
  resources :sessions
  resources :teams

  post '/', to: 'splash#create'

  get '/dashboard', to: 'dashboard#index'

  get '/signout', to: 'sessions#destroy'

  # API
  # ////////////////////////////////////////////////////////////////
  # ////////////////////////////////////////////////////////////////
  namespace :api do
  	namespace :v1 do
        
    	resources :users
    	resources :teams

    end
  end
  # ////////////////////////////////////////////////////////////////
  # ////////////////////////////////////////////////////////////////

  # AngularJS Templates
  # ////////////////////////////////////////////////////////////////
  # ////////////////////////////////////////////////////////////////
  get "angularjs/templates/:template" => 'templates#load_template'
  # ////////////////////////////////////////////////////////////////
  # ////////////////////////////////////////////////////////////////

end